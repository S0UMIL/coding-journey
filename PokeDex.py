class Pokemon:
  def __init__(self, entry , name , types ,description , is_caught):
    self.entry = entry
    self.name = name
    self.types = types
    self.description = description
    self.is_caught = is_caught
Pikachu = Pokemon(25,'Pikachu','Electric','Description:It has small electric sacs on both its cheeks. If threatened, it looses electric charges from the sacs.' ,'Pikachu has already been caught')
def speak(self):
  print(self.name,self.name)
  return(self.name)
def display_details(self):
  print(self.entry)
  print(self.name)
  print(self.types)
  print(self.description)
  print(self.is_caught)
  return(self)
speak(Pikachu)
